package com.khogofinal.domain;

public class Logar {
	
	private String name;
	private String pass;
	
	public Logar() {
		
	}

	public Logar(String name) {
		super();
		this.name = name;
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}
	
	

}
